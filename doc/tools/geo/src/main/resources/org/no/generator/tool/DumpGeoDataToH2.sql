CREATE ALIAS IF NOT EXISTS FTL_INIT FOR "org.h2.fulltext.FullTextLucene.init";
CALL FTL_INIT();

CREATE TABLE Country (
  ID INTEGER NOT NULL,
  COUNTRY CHAR(40) NOT NULL,
  CODE CHAR(2),
  CONTINENT CHAR(25),
  REGION CHAR(25),
  POPULATION INTEGER,
  GEOMETRY GEOMETRY,
  PRIMARY KEY (id)
);
CALL FTL_CREATE_INDEX('PUBLIC', 'COUNTRY', 'COUNTRY, CODE, CONTINENT, REGION');

CREATE TABLE City (
  ID INTEGER NOT NULL,
  CITY CHAR(40) NOT NULL,
  COUNTRY CHAR(40) NOT NULL,
  CODE CHAR(2),
  POPULATION INTEGER,
  LATITUDE FLOAT,
  LONGITUDE FLOAT,
  GEOMETRY GEOMETRY,
  PRIMARY KEY (id)
);
CALL FTL_CREATE_INDEX('PUBLIC', 'CITY', 'CITY, COUNTRY, CODE');
